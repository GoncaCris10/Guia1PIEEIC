void init_UART3()
{
	HAL_UART_Receive_IT(&huart3, &inbuffer[UART3Rx_index], 1);
	control_flag=1;
}

void HAL_UART_RxCpltCallback(UART_HandleTypeDef * huart){
	if(huart->Instance == USART3)//current UART?{
		if(control_flag) {UART3Rx_index ++;}
		UART3Rx_index &= ~(1<<16);//mantem o index dentro dos limites 0 a 7
									// da set ao interrupt do UART3 Rx outra vez
	HAL_UART_Receive_IT(&huart3,&inbuffer[UART3Rx_index],1);
	if(!control_flag) {UART3Rx_index++;}
	if(inbuffer[UART3Rx_index-1] =='\r') {
		RX_string_ready = 1;
		control_flag=0;
		UART3Rx_index=0;
	}
}

void HAL_UART_TxCpltCallback(UART_HandleTypeDef * huart){
	if(huart->Instance == USART3){//current UART?
		HAL_UART_Transmit_IT(&huart3,&outbuff[UART3Tx_index],1);
		UART3Tx_index++;
		}
}

int __io_putchar(int ch){
    HAL_UART_Transmit(&huart3, (uint8_t*) &ch,1,100);
    return ch;
}

